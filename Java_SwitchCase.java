package zJavaAdvanced;

public class Java_SwitchCase {

	public static void main(String[] args) {
	 
		int Id = 10000;
		
		switch (Id) {
		
		case 50: {
			System.out.println("50");
			break;
		}
		case 100: {
			System.out.println("10");
			break;
		}
		case 1000: {
			System.out.println("100");
			break;
		}
		default : System.out.println("no case executed");
		
			
		}

	}

}
